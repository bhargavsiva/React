import { createSlice } from "@reduxjs/toolkit";

const cartSlice = createSlice({
  name: "cart",
  initialState: {
    items: [],
  },
  reducers: {
    addItem: (state, action) => {
      //mutating the state here -
      /**
       * Vanilla older redux doesnt allow reduction in state
       * create a new state and return the new state.
       *
       * Now just modify directly allowed in RTK. But redux is
       * internally doing the same still!!!
       * Redux user immer library to do that
       */

      state.items.push(action.payload);
    },
    removeItem: (state, action) => {
      state.items.pop();
    },
    clearCart: (state, action) => {
      state.items.length = 0;
    },
  },
});

export const { addItem, removeItem, clearCart } = cartSlice.actions;

export default cartSlice.reducer;
